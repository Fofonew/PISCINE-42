char	*ft_strlowcase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 0 && str[i] <= 64) || (str[i] >= 91 && str[i] <= 96)
				|| (str[i] >= 123))
			while ((str[i] >= 0 && str[i] <= 64)
					|| (str[i] >= 91 && str[i] <= 96) || (str[i] >= 123))
				i++;
		if (str[i] >= 'a' && str[i] <= 'z')
			i++;
		if (str[i] >= 'A' && str[i] <= 'Z')
			while (str[i] >= 'A' && str[i] <= 'Z')
			{
				str[i] = str[i] + 32;
				i++;
			}
	}
	return (str);
}
